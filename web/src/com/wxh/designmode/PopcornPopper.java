package com.wxh.designmode;

/**
 * 爆玉米花
 * @author wxh
 * @version $Id: PopcornPopper.java, v 0.1 2017年5月22日 下午3:04:57 wxh Exp $
 */
public class PopcornPopper {

    /**
     * 打开爆米花机
     */
    public void on() {
        System.out.println("popper on");
    }

    /**
     * 开始爆米花
     */
    public void pop() {
        System.out.println("popper pop");
    }

    /**
     * 关闭爆米花机
     */
    public void off() {
        System.out.println("popper off");
    }

}
